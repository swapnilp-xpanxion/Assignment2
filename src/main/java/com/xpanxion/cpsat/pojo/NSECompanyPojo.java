package com.xpanxion.cpsat.pojo;

public class NSECompanyPojo {
    private String symbol;
    private String ltp;
    private String percentChange;
    private String tradedQuantity;
    private String valueLakhs;
    private String open;
    private String high;
    private String low;
    private String prevClose;
    private String latestExDate;

    public String getSymbol() {
        return symbol;
    }

    public void setSymbol(String symbol) {
        this.symbol = symbol;
    }

    public String getLtp() {
        return ltp;
    }

    public void setLtp(String ltp) {
        this.ltp = ltp;
    }

    public String getPercentChange() {
        return percentChange;
    }

    public void setPercentChange(String percentChange) {
        this.percentChange = percentChange;
    }

    public String getTradedQuantity() {
        return tradedQuantity;
    }

    public void setTradedQuantity(String tradedQuantity) {
        this.tradedQuantity = tradedQuantity;
    }

    public String getValueLakhs() {
        return valueLakhs;
    }

    public void setValueLakhs(String valueLakhs) {
        this.valueLakhs = valueLakhs;
    }

    public String getOpen() {
        return open;
    }

    public void setOpen(String open) {
        this.open = open;
    }

    public String getHigh() {
        return high;
    }

    public void setHigh(String high) {
        this.high = high;
    }

    public String getLow() {
        return low;
    }

    public void setLow(String low) {
        this.low = low;
    }

    public String getPrevClose() {
        return prevClose;
    }

    public void setPrevClose(String prevClose) {
        this.prevClose = prevClose;
    }

    public String getLatestExDate() {
        return latestExDate;
    }

    public void setLatestExDate(String latestExDate) {
        this.latestExDate = latestExDate;
    }


}
